package entity;

/**
 * 部门表
 */
public class Dept {
    /* 部门ID */
	private String deptid;
	/* 部门名称 */
    private String deptname;

	public String getDeptid() {
		return deptid;
	}
	public void setDeptid(String deptid) {
		this.deptid = deptid;
	}
	public String getDeptname() {
		return deptname;
	}
	public void setDeptname(String deptname) {
		this.deptname = deptname;
	}

}
